    public static void deleteFromList(String id){
        JSONArray array = JsonClass.getJsonArray(Constants.ORDERS_FILE);
        
        JSONArray newArray = new JSONArray();
        for(int i=array.size()-1;i>=0;i--){
            JSONArray a = (JSONArray) array.get(i);
            JSONObject obj = (JSONObject) a.get(0);
            String gid = obj.get("orderId").toString();
            if(!gid.equals(id)){
                newArray.add(a);
            }
        }
        
        
        FileWriter.write(Constants.ORDERS_FILE, newArray.toString());
        fillTable();
        AlertMessage msg = new AlertMessage("Successfully Deleted");
    }
    
