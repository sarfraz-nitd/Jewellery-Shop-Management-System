    public static void fillTable(){
        tableModel.setNumRows(0);
        JSONArray array = JsonClass.getJsonArray(Constants.ORDERS_FILE);
        for(int i=array.size()-1;i>=0;i--){
            
            JSONArray a = (JSONArray) array.get(i);
            
            JSONObject obj = (JSONObject) a.get(0);
            String orderId = obj.get("orderId").toString();
            String orderBy = obj.get("orderedBy").toString();
            String oDate = obj.get("date").toString();
            String dDate = obj.get("deliveryDate").toString();
            
            tableModel.addRow(new Object[] {orderId, orderBy, oDate, dDate});
            
        }
    }
